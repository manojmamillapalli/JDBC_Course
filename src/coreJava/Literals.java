package coreJava;

public class Literals {
    public static void main(String[] args)
    {
        /*In Java, literal is a value of numeric, boolean, character or a string data. Any constant
        value that can be assigned to the variable is called literal */

        //Integral Literals in Java
        //(*Decimal literals range from 0-9)
        int x=101;
        //(*Octal Literals range from 0 to 7)
        int o=0146;
        //(* Hexadecimal Literals range from  0-9 and characters from a to f)
        int hex=0x4567;
        //(* Binary Literals values are 0 and 1 and preceed with 0b or 0B)
        int bin=0b1;
        System.out.println(bin);

    }

}
